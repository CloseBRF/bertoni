moduleApp.controller('indexCtrl', function($state, $scope, cfpLoadingBar, $rootScope){	
	cfpLoadingBar.start();

	//MENU STATE
	$scope.$state = $state;
	$scope.isMobile = false;

	$scope.detectMob = function() {	
		if (window.innerWidth <= 1024){
			$scope.isMobile = true;
		}
	}; $scope.detectMob();
});  
